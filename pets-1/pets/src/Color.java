/**
 * Enumeration of possible colors for pets in the pet management system.
 * Includes colors (like BLACK, WHITE, BROWN) and pattern
 * types (SPOTTED, MULTICOLOR). Used to track the color of each pet.
 *
 */
public enum Color {
    BLACK,
    WHITE,
    BROWN,
    ORANGE,
    YELLOW,
    GREEN,
    GRAY,
    SPOTTED,
    MULTICOLOR
}
